GENERATE_STEPS_TEMPLATE = """\
请根据以下数学题目和引导等级，生成分步引导计划。

题目：
{problem_text}

涉及知识点：
{knowledge_points}

引导等级：{guidance_level}

请为每一步生成：
1. 一个引导性问题（question）：引导学生思考该步骤
2. 一个知识点提示（hint）：帮助学生回忆相关概念
3. 期望的解答方向（expected_direction）：学生应该得出的结论（内部参考，不展示给学生）
4. 关联的知识点ID（knowledge_point_id）

要求：
- full 级别：每个知识点至少一个步骤，步骤细粒度
- moderate 级别：只保留关键转折点的步骤
- minimal 级别：只保留最核心的一到两个步骤

请以 JSON 数组格式输出，每个元素包含 step_index, question, hint, expected_direction, knowledge_point_id 字段。
"""

